    private void cmbIdKategoriItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbIdKategoriItemStateChanged
        // TODO add your handling code here:
        String nm_kategori = cmbIdKategori.getSelectedItem().toString();
        if (!nm_kategori.equals("")) {
            try {
                Connection conn = koneksi.getConnection();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet sql = stm.executeQuery("SELECT id FROM indkategori WHERE nama='" + nm_kategori + "'");
                if (sql.next()) {
                    txtid_kategori.setText(sql.getString("id"));
                }
                koneksi.closeConnection();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error " + e);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FormTransaksiKeluar.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            txtid_kategori.setText("");
        }
    }//GEN-LAST:event_cmbIdKategoriItemStateChanged

    private void txtStokKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStokKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) && !(c == KeyEvent.VK_BACK_SPACE)) {
            JOptionPane.showMessageDialog(null, "Inputan tidak boleh menganduk spasi", "Ilegal Input", JOptionPane.ERROR_MESSAGE);
            evt.consume();
        }
    }//GEN-LAST:event_txtStokKeyTyped

