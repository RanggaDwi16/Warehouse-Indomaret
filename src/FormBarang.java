    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String row_id = txtID.getText();
        if (!"0".equals(row_id)) {
            try {
                Connection conn = koneksi.getConnection();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet sql = stm.executeQuery("SELECT indbarang.id, indbarang.kode, indbarang.nama, indbarang.id_kategori, indkategori.nama as kategori, indbarang.satuan, indbarang.stok FROM indbarang JOIN indkategori ON indkategori.id = indbarang.id_kategori WHERE indbarang.id='" + row_id + "'");
                if (sql.next()) {
                    aktif(true);
                    btnTambah.setEnabled(false);
                    String kode = sql.getString("kode");
                    labelAction.setText("Edit Data Barang || " + kode);
                    txtID.setText(sql.getString("id"));
                    txtKode.setText(kode);
                    txtNama.setText(sql.getString("nama"));
                    cmbIdKategori.setSelectedItem(sql.getString("kategori"));
                    txtid_kategori.setText(sql.getString("id_kategori"));
                    txtSatuan.setText(sql.getString("satuan"));
                    txtStok.setText(sql.getString("stok"));
                    txttemp_kode.setText(kode);
                    txtKode.requestFocus();
                }
                koneksi.closeConnection();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error " + e);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Terdapat kesalahan id null!");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION);
        if (ok == 0) {
            try {
                String row_id = txtID.getText();
                Connection conn = koneksi.getConnection();
                java.sql.Statement stm = conn.createStatement();
                stm.executeUpdate("DELETE FROM indbarang WHERE id = '" + row_id + "'");
                JOptionPane.showMessageDialog(null, "Berhasil menghapus data.");
                btnTambah.doClick();
                btnTambah.setEnabled(true);
                BtnEnabled(false);
                aktif(false);
                btnTambah.requestFocus();
                koneksi.closeConnection();
                GetData();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error " + e);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

